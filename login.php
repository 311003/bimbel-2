<?php
session_start();
include 'config.php'; // File konfigurasi database

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $email = $conn->real_escape_string($_POST['email']);
    $password = $_POST['password'];

    // Query untuk memeriksa apakah email terdaftar
    $sql = "SELECT * FROM users WHERE email = '$email'";
    $result = $conn->query($sql);

    if ($result->num_rows > 0) {
        $row = $result->fetch_assoc();

        // Verifikasi password
        if (password_verify($password, $row['password'])) {
            // Set session setelah login berhasil
            $_SESSION['user_id'] = $row['id']; // ID pengguna
            $_SESSION['username'] = $row['username'];  // Nama pengguna
            $_SESSION['email'] = $row['email']; // Email pengguna

            // Arahkan ke index.php
            header("Location: index.php");
            exit();
        } else {
            $error = "Password salah.";
        }
    } else {
        $error = "Email tidak ditemukan.";
    }
}
